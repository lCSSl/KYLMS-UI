      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      // 仓库拓展-仓库方格信息表格数据
      WmsWarehouseExtItemList: [],
      // 弹出层标题
      dialog: {
        title: "",
        type: 0,
        open: false,
      },
      initDialog: {
        title: '',
        type: 0,
        open: false,
      },
      // 是否显示弹出层
      warehouse: {},
      // 状态字典
      statusOptions: [],
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        warehouseId: null,
        itemX: null,
        itemY: null,
        status: null,
      },
      // 表单参数
      form: {},
      // 表单校验
      rules: {
        warehouseId: [
          {required: true, message: "仓库ID不能为空", trigger: "blur"}
        ],
        itemX: [
          {required: true, message: "方格横轴不能为空", trigger: "blur"}
        ],
        itemY: [
          {required: true, message: "方格纵轴不能为空", trigger: "blur"}
        ],
      },
      toggleSearchFormValue: 0,
    };
  },
  created() {
    const params = this.$route.params;
    const warehouseId = params && params.warehouseId;
    this.checkPageParams(params);
    getWarehouse(warehouseId).then((res) => {
      this.warehouse = res.data;
      this.checkPageParams(this.warehouse);
    });
    this.getDicts("sys_common_status").then(response => {
      this.statusOptions = response.data;
    });
  },
  mounted() {
    this.getList();
  },
  methods: {
    checkPageParams(params) {
      const {warehouseId} = params;
      if (warehouseId && warehouseId <= 0) {
        this.$router.back();
        return;
      }
    },
    /** 查询仓库拓展-仓库方格信息列表 */
    getList() {
      this.loading = true;
      listWmsWarehouseExtItem({
        warehouseId: this.warehouse.warehouseId,
        ...this.queryParams,
      }).then((res) => {
        this.WmsWarehouseExtItemList = res.rows;
        this.total = res.total;
        if (res.total <= 0 || res.rows.length <= 0) {
          this.initDialog.open=true;
          this.initDialog.title=`警告-当前未初始化`;
        }
        this.loading = false;
      });
    },
    // 状态字典翻译
    statusFormat(row, column) {
      return this.selectDictLabel(this.statusOptions, row.status);
    },
    // 取消按钮
    cancel() {
      this.dialog.open = false;
      this.reset();
    },
    // 表单重置
    reset() {
      this.form = {
        itemId: null,
        warehouseId: null,
        itemX: null,
        itemY: null,
        status: "0",
        delFlag: null,
        createBy: null,
        createTime: null,
        updateBy: null,
        updateTime: null,
        remark: null
      };
      this.resetForm("form");
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1;
      this.getList();
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm("queryForm");
      this.handleQuery();
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map(item => item.itemId)
      this.single = selection.length !== 1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.reset();
      this.dialog.open = true;
      this.dialog.title = "添加仓库拓展-仓库方格信息";
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset();
      const itemId = row.itemId || this.ids
      getWmsWarehouseExtItem(itemId).then(response => {
        this.form = response.data;
        this.dialog.open = true;
        this.dialog.title = "修改仓库拓展-仓库方格信息";
      });
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs["form"].validate(valid => {
        if (valid) {
          if (this.form.itemId != null) {
            updateWmsWarehouseExtItem(this.form).then(response => {
              this.msgSuccess("修改成功");
              this.dialog.open = false;
              this.getList();
            });
          } else {
            addWmsWarehouseExtItem(this.form).then(response => {
              this.msgSuccess("新增成功");
              this.dialog.open = false;
              this.getList();
            });
          }
        }
      });
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const itemIds = row.itemId || this.ids;
      this.$confirm('是否确认删除仓库拓展-仓库方格信息编号为"' + itemIds + '"的数据项?', "警告", {
        confirmButtonText: "确定",
        cancelButtonText: "取消",
        type: "warning"
      }).then(function () {
        return delWmsWarehouseExtItem(itemIds);
      }).then(() => {
        this.getList();
        this.msgSuccess("删除成功");
      })
    },
    /** 导出按钮操作 */
    handleExport() {
      this.download('wms/ExtItem/export', {
        ...this.queryParams
      }, `wms_WmsWarehouseExtItem.xlsx`)
    },
    toggleSearchForm(toggle) {
      if (toggle >= 0) {
        this.toggleSearchFormValue = toggle;
      }
    },
    handleInitDialogClose() {
      console.log('close')
    },
  }
};
