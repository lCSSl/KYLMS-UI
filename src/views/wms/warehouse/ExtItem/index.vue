export default {
  name: "WmsWarehouseExtItem",
  components: {
    ICol,Icon
  },
  data() {
    return {
      grid: {
        gutter: 24,
        xs: 24,
        sm: 24,
        md: 12,
        lg: 6,
        xl: 6
      },
      // 遮罩层
